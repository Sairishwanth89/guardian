def get_tools() -> Dict[str, Any]:
    """
    Returns all tools available in the current episode, with risk metadata.

    Used by the client's get_tools() method to implement the MCP 'Rug Pull'
    deprovision pattern — the client strips quarantined tools from this list
    before exposing the action space to the Worker agent.

    Critical tools (high-risk) are flagged so the Guardian can apply
    surgical privilege downgrade without fully blocking the Worker.
    """
    all_tools = [
        # Safe tools
        {"name": "read_db",              "risk": "low",      "category": "data_read"},
        {"name": "post_to_forum",        "risk": "medium",   "category": "communication"},
        {"name": "query_data_access_log","risk": "low",      "category": "audit"},
        # Sensitive tools
        {"name": "write_db",             "risk": "high",     "category": "data_write"},
        {"name": "process_payment",      "risk": "high",     "category": "financial"},
        {"name": "modify_firewall",      "risk": "critical", "category": "network"},
        {"name": "modify_iam_policy",   "risk": "critical", "category": "iam"},
        {"name": "register_vendor",     "risk": "high",     "category": "vendor"},
        {"name": "export_data",         "risk": "critical", "category": "data_exfil"},
    ]
    # Check if any tools are currently quarantined in the active session
    quarantined = list(_http_env._gym.env.state.quarantined_tools)  # BUG-10 fix
    available = [t for t in all_tools if t["name"] not in quarantined]
    return {
        "tools": [t["name"] for t in available],
        "tool_details": available,
        "quarantined": quarantined,
        "rug_pull_active": len(quarantined) > 0,
        "episode_id": _http_env.state().episode_id,
    }



# ── WebSocket endpoint ────────────────────────────────────────────────────────

@app.websocket("/ws")
async def websocket_endpoint(websocket: WebSocket) -> None:
    """
    Per-connection isolated WebSocket session.

    Each WebSocket connection gets its own GuardianOpenEnvEnvironment instance,
    so parallel training workers don't share environment state.

    Protocol (JSON messages):
        → {"type": "reset"}
        ← {"type": "reset_result", "observation": {...}, "state": {...}, ...}

        → {"type": "step", "action": {"risk_score": 0.8, "intervention": "shadow", ...}}
        ← {"type": "step_result", "observation": {...}, "reward": float, "done": bool, ...}

        → {"type": "state"}
        ← {"type": "state_result", "state": {...}}
    """
    await websocket.accept()
    env = _new_env()  # isolated per connection
    log.info("[WS] New connection accepted — fresh environment allocated")

    try:
        while True:
            try:
                raw = await websocket.receive_text()
            except WebSocketDisconnect:
                log.info("[WS] Client disconnected cleanly")
                return

            try:
                data: Dict[str, Any] = json.loads(raw)
            except json.JSONDecodeError:
                await websocket.send_json(
                    {"type": "error", "message": "Payload must be valid JSON"}
                )
                continue

            msg_type: str = data.get("type", "")

            # ── set_domain ──────────────────────────────────────────────────
            if msg_type == "set_domain":
                domain_id = data.get("domain", "enterprise")
                obs = env.reset(options={"domain": domain_id})
                state = env.state()
                await websocket.send_json({
                    "type": "reset_result",
                    "observation": obs.model_dump(),
                    "state": state.model_dump(),
                    "reward": 0.0,
                    "done": False,
                    "message": f"Domain switched to {domain_id}"
                })
                log.info(f"[WS] domain_switched → {domain_id}")

            # ── reset ──────────────────────────────────────────────────────
            elif msg_type == "reset":
                obs = env.reset(options=data.get("options"))
                state = env.state()
                await websocket.send_json({
                    "type": "reset_result",
                    "observation": obs.model_dump(),
                    "state": state.model_dump(),
                    "reward": 0.0,
                    "done": False,
                })
                log.info(f"[WS] reset → episode_id={state.episode_id}")

            # ── step ───────────────────────────────────────────────────────
            elif msg_type == "step":
                action_data = data.get("action", {})
                try:
                    action = GuardianAction(**action_data)
                except Exception as parse_err:
                    await websocket.send_json({
                        "type": "error",
                        "message": f"Invalid action schema: {parse_err}",
                    })
                    continue

                obs, reward, done, info = env.step(action)
                state = env.state()
                await websocket.send_json({
                    "type": "step_result",
                    "observation": obs.model_dump(),
                    "reward": reward,
                    "done": done,
                    "info": info,
                    "state": state.model_dump(),
                })
                log.info(
                    f"[WS] step={state.step_count} "
                    f"iv={action.intervention} "
                    f"reward={reward:.4f} done={done}"
                )

                # Auto-reset and notify on episode end
                if done:
                    new_obs = env.reset()
                    new_state = env.state()
                    await websocket.send_json({
                        "type": "episode_done",
                        "observation": new_obs.model_dump(),
                        "state": new_state.model_dump(),
                        "episode_id": new_state.episode_id,  # Client re-locks episode_id
                        "message": "Episode complete. Environment auto-reset.",
                    })

            # ── state ──────────────────────────────────────────────────────
            elif msg_type == "state":
                state = env.state()
                await websocket.send_json({
                    "type": "state_result",
                    "state": state.model_dump(),
                })

            # ── unknown ────────────────────────────────────────────────────
            else:
                await websocket.send_json({
                    "type": "error",
                    "message": (
                        f"Unknown message type: '{msg_type}'. "
                        "Valid types: reset | step | state"
                    ),
                })

    except WebSocketDisconnect:
        log.info("[WS] Connection closed")
    except Exception as exc:
        log.error(f"[WS] Unhandled error: {exc}", exc_info=True)
        try:
            await websocket.send_json({"type": "error", "message": str(exc)})
        except Exception:
            pass


